CREATE OR REPLACE PROCEDURE SaveDepartmentChanges(
    p_old_department_id IN DEPARTMENTS.department_id%TYPE,
    p_old_department_name IN DEPARTMENTS.department_name%TYPE,
    p_updated_id IN DEPARTMENTS.department_id%TYPE,
    p_updated_name IN DEPARTMENTS.department_name%TYPE
) AS
BEGIN
    IF p_updated_id = p_old_department_id THEN
        UPDATE DEPARTMENTS
        SET department_name = p_updated_name
        WHERE department_id = p_updated_id;
    ELSIF p_updated_id != p_old_department_id AND p_updated_name = p_old_department_name THEN
        UPDATE DEPARTMENTS
        SET department_id = p_updated_id
        WHERE department_name = p_updated_name;
    ELSE
        INSERT INTO DEPARTMENTS (department_id, department_name)
        VALUES (p_old_department_id, p_old_department_name);
    END IF;
END;



